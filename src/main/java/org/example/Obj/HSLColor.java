package org.example.Obj;

/**
 * 该类实现HSL对应颜色判断
 *
 * @author 吴鹄远
 * Date 2023/12/16 22:57
 */
public enum HSLColor {
    /**
     * 对应红色
     */
    Red,
    /**
     * 对应橙色
     */
    Orange,
    /**
     * 对应黄色
     */
    Yellow,
    /**
     * 对应绿色
     */
    Green,
    /**
     * 对应洋红色
     */
    Cyan,
    /**
     * 对应蓝色
     */
    Blue,
    /**
     * 对应紫色
     */
    Purple
}
